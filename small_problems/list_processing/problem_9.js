 // input: two arguments: inventory item - number, and an array
 // output:  returns an array containing only the transactions for the specified
 // inventory item.
 // algorithm: loop through the array,select the elements that have an id equal to
 // the transaction id.


 function transactionsFor(itemId, transactionList) {
   return transactionList.filter(function (item) {
     return item["id"] === itemId;
   });
 }



 var transactions = [ { id: 101, movement: 'in',  quantity:  5 },
                     { id: 105, movement: 'in',  quantity: 10 },
                     { id: 102, movement: 'out', quantity: 17 },
                     { id: 101, movement: 'in',  quantity: 12 },
                     { id: 103, movement: 'out', quantity: 15 },
                     { id: 102, movement: 'out', quantity: 15 },
                     { id: 105, movement: 'in',  quantity: 25 },
                     { id: 101, movement: 'out', quantity: 18 },
                     { id: 102, movement: 'in',  quantity: 22 },
                     { id: 103, movement: 'out', quantity: 15 }, ];

console.log(transactionsFor(101, transactions));
